package com.experiment06;

public class Container {
    private int id;
    private int w;

    public Container(int id, int w) {
        this.id = id;
        this.w = w;
    }

    public Container() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }
}
